    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        // TODO add your handling code here:
        String username=txtuser.getText();
        try{
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/multiuserlogin", "root", "");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("SELECT securityQ FROM `multiuserlogin` WHERE username='"+username+"'");
            if(rs.next()){
                securityq.setText(rs.getString(1));
            }else{
                getToolkit().beep();
                JOptionPane.showMessageDialog(null, "Please Enter correct username.");
                con.close();
                rs.close();
            }
        }catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchbtnActionPerformed

