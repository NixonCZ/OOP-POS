    private void resetbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetbtnActionPerformed
        // TODO add your handling code here:
        String username=txtuser.getText();
        String newPassword=txtPassword.getText();
        String answer=answertxt.getText();
        try{
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/multiuserlogin", "root", "");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("SELECT * FROM `multiuserlogin` WHERE username='"+username+"' AND securityAns='"+answer+"'");
            if(rs.next()){
                st.executeUpdate("UPDATE `multiuserlogin` SET password='"+newPassword+"' WHERE username='"+username+"' AND securityAns='"+answer+"'");
                JOptionPane.showMessageDialog(this,"Password change successfully!");
                getToolkit().beep();
                LoginForm l = new LoginForm();
                l.setVisible(true);
                this.setVisible(false);
            }else{
                getToolkit().beep();
                JOptionPane.showMessageDialog(null,"Plese Enter correct Username or Answer and Try Again!");
            }
            con.close();
        }catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_resetbtnActionPerformed

